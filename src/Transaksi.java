    private void TampilData(){
        try {
            st = cn.createStatement();
            rs = st.executeQuery("SELECT * FROM transaksi");
            
            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("id_transaksi");
            model.addColumn("nama_pelanggan");
            model.addColumn("nama_wisata");
            model.addColumn("tanggal");
            model.addColumn("biaya_wisata");
            
            
            model.getDataVector().removeAllElements();
            model.fireTableDataChanged();
            model.setRowCount(0);
            
            while(rs.next()){
                Object[] data = {
                  rs.getString("id_transaksi"),
                  rs.getString("nama_pelanggan"),
                  rs.getString("nama_wisata"),
                  rs.getString("tanggal"),
                  rs.getString("biaya_wisata")
                };
                model.addRow(data);
                table_Transaksi.setModel(model);
            }
        } catch (Exception e){
            
        }
    }
    
    public void TampilComboPelanggan(){
        try{
            String sql = "SELECT * FROM pelanggan";
            java.sql.Connection conn = (Connection) koneksi.BukaKoneksi();
            java.sql.PreparedStatement pst = conn.prepareCall(sql);
            ResultSet rs = pst.executeQuery();
            
            while(rs.next()){
                combo_Pelanggan.addItem(rs.getString("nama_pelanggan"));
            }
            rs.last();
        }catch (Exception e){
            System.out.println(e.getMessage());
        }
    }
    
