    private void button_SimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_SimpanActionPerformed
        // TODO add your handling code here:
        try{
            st = cn.createStatement();
            if (txt_BiayaWisata.getText().equals("") || txt_IdTransaksi.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Data tidak boleh kosong", "Validasi Data", JOptionPane.INFORMATION_MESSAGE);
                return;
            }
        
            //aksi simpan data
            if(button_Simpan.getText() == "SIMPAN") {
                String cek = "SELECT * FROM transaksi WHERE id_transaksi = '" + txt_IdTransaksi.getText() + "'";
                rs = st.executeQuery(cek);
                if(rs.next()){
                    JOptionPane.showMessageDialog(null, "Ups... id ini sudah ada");
                }else{
                    String sql = "INSERT INTO transaksi VALUES ('" + txt_IdTransaksi.getText() + "','" + combo_Pelanggan.getSelectedItem() + "','" + combo_Wisata.getSelectedItem() + "','" + 
                            txt_Tanggal.getText() + "','" + txt_BiayaWisata.getText()+ "')";
                            
                    st.executeUpdate(sql);
                    JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
                    Bersih();
                    TampilData();
                }
            }else{
                //aksi ubah data
                String update = "UPDATE transaksi SET nama_wisata = '" + combo_Wisata.getSelectedItem() + "', biaya_wisata = '" + txt_BiayaWisata.getText() + "', nama_pelanggan = '" + combo_Pelanggan.getSelectedItem() + "' WHERE id_transaksi = '" + txt_IdTransaksi.getText() + "'";
                st.executeUpdate(update);
                JOptionPane.showMessageDialog(null, "Data berhasil diubah");
                Bersih();
                TampilData();
            }
        } catch (Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_button_SimpanActionPerformed

